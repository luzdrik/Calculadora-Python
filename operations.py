def soma (a,b):
    return a + b

def subtracao (a,b):
    return a - b

def multiplicacao (a,b):
    return a * b

def divisao (a,b):
    return a / b

def potenciacao (a,b):
    return a ** b

def radiciacao (a):
    return a ** (1/2)

def divisao_inteira (a,b):
    return a // b

def divisao_resto (a,b):
    return a % b